var num1 = 10;
var num2 = 5;

var sum = num1 + num2; // Addition
var difference = num1 - num2; // Subtraction
var product = num1 * num2; // Multiplication
var quotient = num1 / num2; // Division
var remainder = num1 % num2; // Modulus
var power = num1 ** num2; // Exponentiation

console.log("Sum:", sum); // Output: 15
console.log("Difference:", difference); // Output: 5
console.log("Product:", product); // Output: 50
console.log("Quotient:", quotient); // Output: 2
console.log("Remainder:", remainder); // Output: 0
console.log("Power:", power); // Output: 100000


console.log(2 ** 3); // Output: 8
// 2 raised to the power of 3 (2 * 2 * 2)

console.log(5 ** 2); // Output: 25
// 5 raised to the power of 2 (5 * 5)


